/* =====================================================
   setLang: 言語を切り替えてlocalStorageに保存
   ===================================================== */
function setLang(lang) {
  if (!LANG_TRANSLATIONS[lang]) return;
  localStorage.setItem('arc_lang', lang);
  var t = LANG_TRANSLATIONS[lang];

  // data-i18n 属性を持つ要素を更新
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // data-i18n-placeholder 属性を持つ要素を更新
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
  });

  // indexページのニュースグリッドを再構築
  var indexNewsGrid = document.getElementById('index-news-grid');
  if (indexNewsGrid && NEWS_ARTICLES[lang]) {
    var articles = NEWS_ARTICLES[lang].slice(0, 4);
    indexNewsGrid.innerHTML = articles.map(function(a, i) {
      return '<a href="news.html#article-' + i + '" class="news-item">' +
        '<span class="news-date">' + a.date + '</span>' +
        '<span class="news-title">' + a.title + '</span>' +
        '<span class="news-arrow">→</span>' +
        '</a>';
    }).join('');
  }

  // セレクトボックスの表示を更新
  document.querySelectorAll('.lang-select').forEach(function(sel) {
    sel.value = lang;
  });

  // モバイルボタンのアクティブ状態を更新
  document.querySelectorAll('.mobile-lang button').forEach(function(btn) {
    var oc = btn.getAttribute('onclick') || '';
    btn.classList.toggle('active', oc.indexOf("'" + lang + "'") !== -1);
  });

  // ニュースページの記事を再描画
  if (typeof buildNewsList === 'function' && NEWS_ARTICLES[lang]) {
    window.NEWS_DATA = NEWS_ARTICLES[lang];
    buildNewsList();
  }
}

/* =====================================================
   initLang: ページ読み込み時にlocalStorageから言語を復元
   ===================================================== */
function initLang() {
  var lang = localStorage.getItem('arc_lang') || 'ja';
  setLang(lang);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLang);
} else {
  initLang();
}
